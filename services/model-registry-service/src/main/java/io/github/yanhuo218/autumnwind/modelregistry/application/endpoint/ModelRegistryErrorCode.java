package io.github.yanhuo218.autumnwind.modelregistry.application.endpoint;

public enum ModelRegistryErrorCode {
    INVALID_REQUEST("AW-MODEL-REGISTRY-VALIDATION-0001"),
    INVALID_SERVICE_TOKEN("AW-MODEL-REGISTRY-AUTH-0001"),
    ACCESS_DENIED("AW-MODEL-REGISTRY-FORBIDDEN-0001"),
    ENDPOINT_NOT_FOUND("AW-MODEL-REGISTRY-NOT_FOUND-0001"),
    ENDPOINT_VERSION_CONFLICT("AW-MODEL-REGISTRY-CONFLICT-0001"),
    ENDPOINT_SECRET_FAILURE("AW-MODEL-REGISTRY-DEPENDENCY-0001"),
    MODEL_NOT_FOUND("AW-MODEL-REGISTRY-NOT_FOUND-0002"),
    MODEL_CONFLICT("AW-MODEL-REGISTRY-CONFLICT-0002"),
    DEFAULT_MODEL_CONFLICT("AW-MODEL-REGISTRY-CONFLICT-0003"),
    MODEL_VERSION_CONFLICT("AW-MODEL-REGISTRY-CONFLICT-0004"),
    INTERNAL_ERROR("AW-MODEL-REGISTRY-INTERNAL-0001");

    private final String value;

    ModelRegistryErrorCode(String value) {
        this.value = value;
    }

    public String value() {
        return value;
    }
}
